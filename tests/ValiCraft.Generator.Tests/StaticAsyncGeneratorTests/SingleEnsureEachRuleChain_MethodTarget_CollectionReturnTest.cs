                                                                               }

                                                                               private static async global::System.Threading.Tasks.Task<global::System.Collections.Generic.List<global::ErrorCraft.IValidationError>?> RunValidationLogicAsync(global::Test.Requests.Inventory request, string? inheritedTargetPath, global::System.Threading.CancellationToken cancellationToken)

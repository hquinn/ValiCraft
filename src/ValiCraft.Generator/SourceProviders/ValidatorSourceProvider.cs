using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using ValiCraft.Generator.Concepts;
using ValiCraft.Generator.Models;
using ValiCraft.Generator.RuleChains;
using ValiCraft.Generator.RuleChains.Context;
using ValiCraft.Generator.Types;

namespace ValiCraft.Generator.SourceProviders;

public static class ValidatorSourceProvider
{
    public static void EmitSourceCode(
        ImmutableArray<ProviderResult<Validator>> validatorResults,
        SourceProductionContext context)
    {
        foreach (var validatorResult in validatorResults)
        {
            if (HasReportedDiagnostics(validatorResult, context))
            {
                continue;
            }

            var sourceCode = GenerateSourceCode(validatorResult.Value!);
            var fileName = GetFileName(validatorResult.Value!);

            context.AddSource(fileName, SourceText.From(sourceCode, Encoding.UTF8));
        }
    }

    private static string GetFileName(Validator validator)
    {
        var containingTypesPrefix = string.Join(".", validator.Class.ContainingTypes.Select(ct => ct.Name));
        
        if (!string.IsNullOrEmpty(containingTypesPrefix))
        {
            return $"{validator.Class.Namespace}.{containingTypesPrefix}.{validator.Class.Name}.g.cs";
        }
        
        return $"{validator.Class.Namespace}.{validator.Class.Name}.g.cs";
    }

    private static bool HasReportedDiagnostics(
        ProviderResult<Validator> validatorResult,
        SourceProductionContext context)
    {
        if (validatorResult.Diagnostics is not { Count: > 0 })
        {
            return false;
        }
        
        foreach (var diagnostic in validatorResult.Diagnostics)
        {
            context.ReportDiagnostic(diagnostic.CreateDiagnostic());
        }

        return true;
    }

    private static string GenerateSourceCode(Validator validator)
    {
        var usingDirectivesBuilder = new StringBuilder();

        foreach (var usingDirective in validator.UsingDirectives)
        {
            usingDirectivesBuilder.AppendLine(usingDirective);
        }

        var validatorClassContent = GenerateValidatorClassContent(validator);
        var wrappedContent = WrapInContainingTypes(validator.Class.ContainingTypes, validatorClassContent);

        return $$"""
                 // <auto-generated />
                 #nullable enable

                 {{usingDirectivesBuilder}}
                 namespace {{validator.Class.Namespace}}
                 {
                 {{wrappedContent}}
                 }
                 """;
    }

    private static string WrapInContainingTypes(EquatableArray<ContainingTypeInfo> containingTypes, string innerContent)
    {
        if (containingTypes.Count == 0)
        {
            return innerContent;
        }

        var result = innerContent;
        
        // Start from innermost and wrap outward (reverse order since containingTypes is outermost-first)
        for (var i = containingTypes.Count - 1; i >= 0; i--)
        {
            var containingType = containingTypes[i];
            var indentedContent = IndentContent(result, "    ");
            result = $"    {containingType.Modifiers} class {containingType.Name}\n    {{\n{indentedContent}\n    }}";
        }

        return result;
    }

    private static string IndentContent(string content, string indent)
    {
        var lines = content.Split('\n');
        var indentedLines = lines.Select(line => string.IsNullOrWhiteSpace(line) ? line : indent + line);
        return string.Join("\n", indentedLines);
    }

    private static string GenerateValidatorClassContent(Validator validator)
    {
        var isAsync = validator.IsAsync;
        var isStatic = validator.IsStatic;
        var requestType = validator.RequestTypeName.FullyQualifiedName;

        var staticKeyword = isStatic ? "static " : "";
        var asyncKeyword = isAsync ? "async " : "";
        var interfaceName = $"global::{KnownNames.Interfaces.GetValidatorInterface(isAsync, isStatic)}";
        var validateMethod = KnownNames.Methods.GetValidateMethod(isAsync);
        var runValidationMethod = KnownNames.Methods.GetRunValidationMethod(isAsync);
        var runValidationLogicMethod = KnownNames.Methods.GetRunValidationLogicMethod(isAsync);

        string returnType;
        string listReturnType;
        string cancellationParam;
        string cancellationArg;
        string awaitPrefix;

        if (isAsync)
        {
            returnType = $"global::System.Threading.Tasks.Task<global::{KnownNames.Types.ValidationErrors}?>";
            listReturnType = $"global::System.Threading.Tasks.Task<global::System.Collections.Generic.List<global::{KnownNames.Interfaces.IValidationError}>?>";
            cancellationParam = ", global::System.Threading.CancellationToken cancellationToken";
            cancellationArg = ", cancellationToken";
            awaitPrefix = "await ";
        }
        else
        {
            returnType = $"global::{KnownNames.Types.ValidationErrors}?";
            listReturnType = $"global::System.Collections.Generic.List<global::{KnownNames.Interfaces.IValidationError}>?";
            cancellationParam = "";
            cancellationArg = "";
            awaitPrefix = "";
        }

        var publicCancellationParam = isAsync ? ", global::System.Threading.CancellationToken cancellationToken = default" : "";

        return $$"""
                /// <summary>
                /// Generated {{(isStatic ? "static " : "")}}{{(isAsync ? "async " : "")}}validator for <see cref="{{requestType}}"/>.
                /// </summary>
                {{validator.Class.Modifiers}} class {{validator.Class.Name}} : {{interfaceName}}<{{requestType}}>
                {
                    /// <inheritdoc />
                    public {{staticKeyword}}{{asyncKeyword}}{{returnType}} {{validateMethod}}({{requestType}} request{{publicCancellationParam}})
                    {
                        var errors = {{awaitPrefix}}{{runValidationLogicMethod}}(request, null{{cancellationArg}});

                        if (errors is null) return null;

                        return new global::{{KnownNames.Types.ValidationErrors}}
                        {
                            Code = "{{validator.RequestTypeName.Name}}Errors",
                            Message = "One or more validation errors occurred.",
                            Severity = global::{{KnownNames.Enums.ErrorSeverity}}.Error,{{GetMetadataProperty(validator)}}
                            Errors = errors
                        };
                    }

                    /// <inheritdoc />
                    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]
                    public {{staticKeyword}}{{asyncKeyword}}{{listReturnType}} {{runValidationMethod}}({{requestType}} request, string? inheritedTargetPath{{cancellationParam}})
                    {
                        return {{awaitPrefix}}{{runValidationLogicMethod}}(request, inheritedTargetPath{{cancellationArg}});
                    }

                    private {{staticKeyword}}{{asyncKeyword}}{{listReturnType}} {{runValidationLogicMethod}}({{requestType}} request, string? inheritedTargetPath{{cancellationParam}})
                    {
                        global::System.Collections.Generic.List<global::{{KnownNames.Interfaces.IValidationError}}>? errors = null;

            {{GenerateCodeForRuleChains(validator.RuleChains)}}

                        return errors;
                    }
                }
            """;
    }

    private static string GenerateCodeForRuleChains(EquatableArray<RuleChain> ruleChains)
    {
        var ruleChainsCode = new List<string>();
        var context = new RuleChainContext(ruleChains.Sum(rc => rc.Config.NumberOfRules));

        foreach (var ruleChain in ruleChains)
        {
            ruleChainsCode.Add(ruleChain.GenerateCode(context));
        }

        return string.Join("\r\n\r\n", ruleChainsCode);
    }

    private static string GetMetadataProperty(Validator validator)
    {
        if (!validator.IncludeDefaultMetadata)
        {
            return "";
        }

        return $"\n                                                                                           Metadata = new global::System.Collections.Generic.Dictionary<string, object?>\n                                                                                           {{\n                                                                                               {{ \"RequestType\", \"{validator.RequestTypeName.Name}\" }},\n                                                                                               {{ \"ValidationCount\", errors.Count }}\n                                                                                           }},";
    }
}